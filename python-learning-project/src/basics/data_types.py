"""Data types examples"""
# Lists
fruits = ["apple", "banana", "cherry"]
print(f"Fruits: {fruits}")
fruits.append("orange")
print(f"After append: {fruits}")

# Dictionaries
person = {"name": "John", "age": 30}
print(f"Person: {person}")

# List comprehension
squares = [x**2 for x in range(1, 6)]
print(f"Squares: {squares}")
